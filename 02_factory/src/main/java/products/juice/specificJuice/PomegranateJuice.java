class PomegranateJuice implements Juice {
  protected double capacity;

  public PomegranateJuice() {}

  public PomegranateJuice(double capacity) {
    this.capacity = capacity;
  }

  public void setCapacity() {
    this.capacity = capacity;
  }
  public double getCapacity() {
    return capacity;
  }
}
